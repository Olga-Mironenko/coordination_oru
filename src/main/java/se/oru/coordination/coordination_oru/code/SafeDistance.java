package se.oru.coordination.coordination_oru.code;

public class SafeDistance {
    public double front;
    public double back;
    public double left;
    public double right;

    public SafeDistance(double front, double back, double left, double right) {
        this.front = front;
        this.back = back;
        this.left = left;
        this.right = right;
    }
}
